-- Growth Ecosystem: new tables + extended columns
-- Adds: growth_thread, weekly_mirrors tables
-- Extends: daily_sessions, user_insights, user_journeys

-- ─── NEW TABLE: growth_thread ──────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS growth_thread (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  date DATE NOT NULL,
  label TEXT NOT NULL,
  type TEXT NOT NULL CHECK (type IN ('milestone', 'breakthrough', 'pattern', 'mirror', 'pinned')),
  journey_id TEXT,
  detail TEXT,
  created_at TIMESTAMPTZ DEFAULT now()
);

ALTER TABLE growth_thread ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users access own growth_thread"
  ON growth_thread FOR ALL
  USING (auth.uid() = user_id)
  WITH CHECK (auth.uid() = user_id);

CREATE INDEX IF NOT EXISTS idx_growth_thread_user_id
  ON growth_thread (user_id, date DESC);

-- ─── NEW TABLE: weekly_mirrors ─────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS weekly_mirrors (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  week_start DATE NOT NULL,
  narrative_text TEXT NOT NULL,
  practice_count INTEGER DEFAULT 0,
  practices_felt_natural INTEGER DEFAULT 0,
  key_patterns JSONB,
  created_at TIMESTAMPTZ DEFAULT now(),
  UNIQUE (user_id, week_start)
);

ALTER TABLE weekly_mirrors ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users access own weekly_mirrors"
  ON weekly_mirrors FOR ALL
  USING (auth.uid() = user_id)
  WITH CHECK (auth.uid() = user_id);

CREATE INDEX IF NOT EXISTS idx_weekly_mirrors_user_id
  ON weekly_mirrors (user_id, week_start DESC);

