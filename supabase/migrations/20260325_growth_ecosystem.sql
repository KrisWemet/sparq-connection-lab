COMMENT ON COLUMN daily_sessions.evening_emotional_tone IS 'AI-detected emotional tone from evening check-in (e.g. hopeful, frustrated, calm).';

-- ─── EXTEND: user_insights ─────────────────────────────────────────────────────
DO $$ BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'user_insights' AND column_name = 'next_greeting_text'
  ) THEN
    ALTER TABLE user_insights ADD COLUMN next_greeting_text TEXT;
  END IF;
END $$;

DO $$ BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'user_insights' AND column_name = 'journey_completion_state'
  ) THEN
    ALTER TABLE user_insights ADD COLUMN journey_completion_state TEXT
      CHECK (journey_completion_state IN ('pending_decision', 'resting'));
  END IF;
END $$;

DO $$ BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'user_insights' AND column_name = 'recommended_next_journeys'
  ) THEN
    ALTER TABLE user_insights ADD COLUMN recommended_next_journeys JSONB;
  END IF;
END $$;

COMMENT ON COLUMN user_insights.next_greeting_text IS 'Pre-generated Peter greeting for next dashboard visit. Fire-and-forget at session end.';
COMMENT ON COLUMN user_insights.journey_completion_state IS 'Post-journey state: pending_decision (awaiting choice) or resting (rest day). NULL = in journey or pre-journey.';
COMMENT ON COLUMN user_insights.recommended_next_journeys IS 'Array of recommended journey IDs after completion. Generated by next-journey-recommender.';

