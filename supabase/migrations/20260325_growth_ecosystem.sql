  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'daily_sessions' AND column_name = 'practice_attempted'
  ) THEN
    ALTER TABLE daily_sessions ADD COLUMN practice_attempted BOOLEAN;
  END IF;
END $$;

DO $$ BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'daily_sessions' AND column_name = 'evening_emotional_tone'
  ) THEN
    ALTER TABLE daily_sessions ADD COLUMN evening_emotional_tone TEXT;
  END IF;
END $$;

